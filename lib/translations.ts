const dicts: Record<Locale, Dict> = { en, fr };

export function t(locale: Locale, key: string, ...args: (string | number)[]): string {
  const str = dicts[locale]?.[key] ?? dicts.en?.[key] ?? key;
  if (args.length === 0) return str;
  return str.replace(/\{(\d+)\}/g, (_, idx) => String(args[Number(idx)] ?? ""));
}
